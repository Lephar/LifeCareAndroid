package fit.lifecare.lifecare.DatabaseClasses;

public class FormulaData {
    
    
    private Double K;
    private Double L;
    private Double M;
    private Double N;
    private Double O;
    private Double T;
    private Double X;
    private Double Y;
    private Double Z;
    
    public FormulaData() {
    }
    
    public FormulaData(Double k, Double l, Double m, Double n, Double o, Double t, Double x, Double y, Double z) {
        K = k;
        L = l;
        M = m;
        N = n;
        O = o;
        T = t;
        X = x;
        Y = y;
        Z = z;
    }
    
    public Double getK() {
        return K;
    }
    
    public void setK(Double k) {
        K = k;
    }
    
    public Double getL() {
        return L;
    }
    
    public void setL(Double l) {
        L = l;
    }
    
    public Double getM() {
        return M;
    }
    
    public void setM(Double m) {
        M = m;
    }
    
    public Double getN() {
        return N;
    }
    
    public void setN(Double n) {
        N = n;
    }
    
    public Double getO() {
        return O;
    }
    
    public void setO(Double o) {
        O = o;
    }
    
    public Double getT() {
        return T;
    }
    
    public void setT(Double t) {
        T = t;
    }
    
    public Double getX() {
        return X;
    }
    
    public void setX(Double x) {
        X = x;
    }
    
    public Double getY() {
        return Y;
    }
    
    public void setY(Double y) {
        Y = y;
    }
    
    public Double getZ() {
        return Z;
    }
    
    public void setZ(Double z) {
        Z = z;
    }
}
